            산업통상자원부는 15일 올해 상반기 FDI가 신고 기준 110억9,000만 달러로, 전년 동기 대비 15.6% 감소했다고 밝혔다. 실제 도착 기준으로는 21.1% 감소한 69억2,000달러를
            기록했다. 투자하겠다 신고는 했지만, 실제 투자는 약 62%만 이뤄진 것이다.

            산업부는 올해 초 발발한 우크라이나 전쟁이 상반기 내내 이어진 데다 글로벌 인플레이션과 금리 인상까지 잇따르면서 투자 불확실성이 커져 FDI가 감소한 것으로 설명했다. 다만 지난해 상반기에 역대
            최대 실적을 기록한 데 따른 기저 효과를 감안하면, 비관할 상황은 아니라는 게 정부 관계자의 설명이다. 실제 신종 코로나바이러스 감염증(코로나19) 발생 이전인 2019년 대비로는 12.3%
            증가해 역대 3위를 기록했다.

            세부적으로도 유럽연합(EU)발 투자와 서비스업 투자가 각각 73.2%와 26.3% 감소했지만, 제조업(53.3%)과 그린필드 투자(9.1%)가 확대됐고, 투자 신고·도착 건수가 늘어난 점은
            긍정적이다. 그린필드 투자는 국내에 공장이나 사업장을 짓는 방식으로 이뤄지는 투자다.

            업종별로는 FDI에서 78.9%의 가장 큰 비중을 차지하는 서비스업 신고 금액이 76억4,000만 달러로 전년 동기 대비 26.3% 감소했다. 운수·창고(268.6%),
            여가·스포츠·오락(190.6%), 부동산(61.4%) 등의 투자가 늘어난 반면, 사업지원·임대(-99.4%), 숙박·음식(-74.9%), 정보통신(-27.4%) 등의 업종에서 투자가 감소하면서
            전반적인 감소세를 나타난 것으로 보인다.

