            3억4,500만 달러로 53.7% 감소했다.

            반면 제조업은 신고액이 31억 달러로 지난해보다 53.3% 증가했다. 제조업은 전체 FDI에서 15.4%를 차지한다. 제조업 내에서는 의약(1,779.4%), 식품(484.4%),
            제지·목재(353.3%), 기계장비·의료정밀(196.0%), 금속(74.1%) 등의 업종에서 투자가 큰 폭으로 늘었다.

